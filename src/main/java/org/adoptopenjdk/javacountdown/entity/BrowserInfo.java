package org.adoptopenjdk.javacountdown.entity;

import java.io.Serializable;

import com.google.code.morphia.annotations.Embedded;

/**
 * 
 * This entity represents the visitor's browser information
 * 
 * @author Alex Theedom
 * 
 */
@Embedded
public class BrowserInfo implements Serializable {

    private static final long serialVersionUID = 6008518106648845721L;

    private String version;
    private String name;

    public BrowserInfo() {
    }

    public BrowserInfo(String name, String version) {
        this.name = name;
        this.version = version;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        result = prime * result + ((version == null) ? 0 : version.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        BrowserInfo other = (BrowserInfo) obj;
        if (name == null) {
            if (other.name != null)
                return false;
        } else if (!name.equals(other.name))
            return false;
        if (version == null) {
            if (other.version != null)
                return false;
        } else if (!version.equals(other.version))
            return false;
        return true;
    }

    @Override
    public String toString() {
        return "BrowserInfo [version=" + version + ", name=" + name + "]";
    }

}
